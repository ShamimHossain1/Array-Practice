// push used to add an element at the last of an Array
var numbers=[20, 30, 40, 50];
numbers.push(60);
// console.log(numbers);

// add an element at the start of an array

var names=['shamim', 'hamim', 'shajim'];
names.unshift('hasan');
// console.log(names);

// pop used to remove last element from an Array

var price=[100, 200, 300, 400];
// console.log(price);
price.pop();
console.log(price);
var prices=price.pop();
console.log(prices);

